void main(List<String> args) {
  int value1 = 10, value2 = 20;

  if (value1 == value2) print('value1 is equal to value2 ');
  if (value1 != value2) print('value1 isn\'t equal to value2');
  if (value1 > value2) print('value1 greater than value2');
  if (value1 < value2) print('value1 less than value2 ');
  if (value1 >= value2) print('value1 greater than or equal to value2');
  if (value1 <= value2) print('value1 less than or equal to value2');
}
