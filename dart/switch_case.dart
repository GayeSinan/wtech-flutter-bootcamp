void main(List<String> args) {
  int i = 5;
  switch (i) {
    case 0:
      print("i'nin değeri 0'dir.");
      break;
    case 5:
      print("i'nin değeri 5'tir.");
      break;
    case 10:
      print("i'nin değeri 10'dur.");
      break;
    default:
      print("i'nin değeri bilinmiyor.");
      break;
  }
}
